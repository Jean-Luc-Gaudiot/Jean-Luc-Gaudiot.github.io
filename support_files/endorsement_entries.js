export function fillList() {
    let dom = document.getElementById("list");
    let list = "";
    for (let country in countries) {
        let countryEntry = countries[country];
        let countryName = countryEntry.country_name;
        list += '<h3>' + countryName + ':</h3>\n';
        list += '<ol class="countryList">';
        for (let endorsement in countryEntry.endorsements) {
            list += '<li>';
            let endorsementEntry = countryEntry.endorsements[endorsement];
            list += '<ul>';
            let bold = true
            for (let author_line in endorsementEntry.author) {
                if (bold) {
                    list += '<li style="list-style-type: disc">';
                    list += '<b>' + endorsementEntry.author[author_line] + '</b><br />';
                    bold = false;
                } else {
                    list += '<li style="list-style-type: circle">';
                    list += endorsementEntry.author[author_line] + '<br />';
                }

                list += '</li>';
            }
            for (let quote_line in endorsementEntry.quote) {
                list +=
                    '<p>' +
                    endorsementEntry.quote[quote_line] +
                    '</p>';
            }
            list += '</ul>\n'
            list += '</li>\n';
        }
        list += "</ol>\n";
    }
    dom.innerHTML = list;
}
